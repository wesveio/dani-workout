export const catalogByMuscleGroup: CatalogGroup[] = exerciseCatalog.reduce<CatalogGroup[]>(
  (groups, exercise) => {
    const existing = groups.find((g) => g.muscleGroup === exercise.muscleGroup)
    if (existing) {
      existing.exercises.push(exercise)
    } else {
      groups.push({ muscleGroup: exercise.muscleGroup, exercises: [exercise] })
    }
    return groups
  },
  [],
)
